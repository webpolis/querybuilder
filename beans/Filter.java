package es.com.tuv.trip2.hibernate.querybuilder.beans;

public class Filter {
	public static final String DEFAULT_STRING_VALUE = "";
	public static final int DEFAULT_NUMERIC_VALUE = 0;

	protected boolean explicitFilter = false;
	protected String entity = null;
	protected String method = null;
	protected String messageParamName = null;
	protected String defaultStringValue = DEFAULT_STRING_VALUE;
	protected int defaultNumericValue = DEFAULT_NUMERIC_VALUE;
	protected String[] defaultImplicitValues = null;

	public String getEntity() {
		return entity;
	}

	public void setEntity(String entity) {
		this.entity = entity;
	}

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	public String getDefaultStringValue() {
		return defaultStringValue;
	}

	public void setDefaultStringValue(String defaultStringValue) {
		this.defaultStringValue = defaultStringValue;
	}

	public Integer getDefaultNumericValue() {
		return defaultNumericValue;
	}

	public void setDefaultNumericValue(int defaultNumericValue) {
		this.defaultNumericValue = defaultNumericValue;
	}

	public boolean isExplicitFilter() {
		return explicitFilter;
	}

	public void setExplicitFilter(boolean explicitFilter) {
		this.explicitFilter = explicitFilter;
	}

	public String[] getDefaultImplicitValues() {
		return defaultImplicitValues;
	}

	public void setDefaultImplicitValues(String[] defaultImplicitValues) {
		this.defaultImplicitValues = defaultImplicitValues;
	}

	public String getMessageParamName() {
		return messageParamName;
	}

	public void setMessageParamName(String messageParam) {
		this.messageParamName = messageParam;
	}

}
